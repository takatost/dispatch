package com.darna.dispatch.bean;

public class DishList {
	String dish_name, dish_price, dish_pack_price, number;

	public String getDish_name() {
		return dish_name;
	}

	public void setDish_name(String dish_name) {
		this.dish_name = dish_name;
	}

	public String getDish_price() {
		return dish_price;
	}

	public void setDish_price(String dish_price) {
		this.dish_price = dish_price;
	}

	public String getDish_pack_price() {
		return dish_pack_price;
	}

	public void setDish_pack_price(String dish_pack_price) {
		this.dish_pack_price = dish_pack_price;
	}

	public String getNumber() {
		return number;
	}

	public void setNumber(String number) {
		this.number = number;
	}
}
